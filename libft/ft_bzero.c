#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	char	*cast;
	size_t	cont;

	cont = 0;
	cast = (char *) s;
	while (cont < n)
	{
		cast[cont] = '\0';
		cont++;
	}
}
